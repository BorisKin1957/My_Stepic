n, m = [int(i) for i in input().split()]

matrix = [[0] * m for _ in range(n)]
k = 0

for q in range(n + m + 1):
    for i in range(n):
        for j in range(m):
            if i + j == q:
                k += 1
                matrix[i][j] = k

for i in range(n):
    for j in range(m):
        print(str(matrix[i][j]).ljust(3), end=' ')
    print()