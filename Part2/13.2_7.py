'''Юный математик 🌶️

Дима учится в седьмом классе и сейчас они проходят обыкновенные дроби с
натуральными числителем и знаменателем. Вчера на уроке математики Дима
узнал, что дробь называется правильной, если ее числитель
меньше знаменателя, и несократимой, если нет равной ей дроби с
меньшими натуральными числителем и знаменателем.

Дима очень любит математику, поэтому дома он долго экспериментировал,
придумывая и решая разные задачки с правильными несократимыми дробями. О
дну из этих задач Дима предлагает решить вам с помощью компьютера.
На вход программе подается натуральное число nn. Напишите программу,
которая находит наибольшую правильную несократимую дробь с суммой
числителя и знаменателя равной n.

Формат входных данных
На вход программе подается натуральное число n.

Формат выходных данных
Программа должна вывести ответ на задачу.

Примечание. Возможно вам потребуется функция gcd(), которая позволяет находить наибольший общий делитель
(НОД) двух чисел. Функция реализована в модуле math.


1   10 	3/7
2 	23 	11/12
3 	3 	1/2
4 	4 	1/3
5 	5 	2/3
6 	6 	1/5
'''

from fractions import Fraction as F
from math import gcd

a = int(input())

if a % 2 != 0:
    m, n = a // 2, a % 2 + a // 2
else:
    m, n = a // 2 - 1, a // 2 + 1
    if gcd(m, n) > 1:
        m -= 1
        n += 1

result = F(m, n)

print(result)