'''Камень, ножницы, бумага
На первой строке записан выбор Тимура, на второй – выбор Руслана.'''

s = [input() for _ in range(2)]

if s == ['ножницы', 'бумага'] or s == ['бумага', 'камень'] or s == ['камень', 'ножницы']:
    print('Тимур')

elif s == ['ножницы', 'камень'] or s == ['бумага', 'ножницы'] or s == ['камень', 'бумага']:
    print('Руслан')

else:
    print('Ничья')