# Ввод числа и проверка валидности введенного значения
def valid_num(num):
    while num.isdigit() == False:
        print('Будьте внимательнее. ) Введите, пожалуйста, только число!', '\n')
        num = input()
    num = int(num)
    while num > 100:
        print('Число должно быть не более 100!', '\n')
        num = int(input())
    return num


# Ввод числа
def input_num(answer, num):
    print('Введите число от', answer, 'до', num, '\n' * 2)
    num = input()
    return num


# Проверяем число на результат
def good_answer(num, answer):
    count = 1
    num_min = 0
    num_max = 100
    while num != answer:

        if num < answer:
            num_min = num
            print('Это ваша', count, 'попытка,', 'пока слишком мало, попробуйте еще раз')
            num = input_num(num, num_max)
            num = valid_num(num)

        elif num > answer:
            num_max = num
            print('Это ваша', count, 'попытка,', 'уже слишком много, попробуйте еще раз')
            num = input_num(num_min, num)
            num = valid_num(num)

        count += 1
    else:
        print('Это была ваша', count, 'попытка.', 'Вы угадали, поздравляем!')


# Основная программа
print('Введите число от 0 до 100', '\n' * 2)
num = input()
num = valid_num(num)

import random

answer = random.randint(0, 100)

print()

good_answer(num, answer)
