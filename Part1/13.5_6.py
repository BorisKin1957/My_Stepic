def is_palindrome(text):
    s1 = text.replace(' ', '')
    s = s1.replace(',', '')
    s1 = s.replace('.', '')
    s = s1.replace('-', '')
    s1 = s.replace('!', '')
    s = s1.replace('?', '')

    j = - 1
    flag = True

    for i in range(len(s)):
        if s.lower()[i] != s.lower()[j]:
            flag = False
            break
        j -= 1

    return flag


