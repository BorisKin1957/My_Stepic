import re, sys

D1 = {}
for line in sys.stdin:
    string = line.split('>')
    for word in string:
        regex = r'<([\w+-]+)| ([\w+-]+)='
        result = re.findall(regex, word)

        if len(result) > 0:
            l = []
            for elem in result:
                for i in elem:
                    if i:
                        l.append(i)
        D2 = {}
        if len(l) > 1:
            for i in range(1, len(l)):
                    D2.setdefault(l[0], []).append(l[i])
        else:
            D2 = {l[0]: []}
        D1.update(D2)
        continue

for key in sorted(D1):
    print(f'{key}:', end=' ')
