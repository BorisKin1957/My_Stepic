def is_good_password(txt):
    a = True
    for i in txt:
        if i.isalpha():
            a = False
            break
    if len(txt) < 9:
        raise LengthError('no long')
    elif txt.islower() or txt.isupper() or txt.isdigit() or a:
        raise LetterError('bad chars')
    elif txt.isalpha():
        raise DigitError('no digit')
    return True

class PasswordError(Exception):
    pass

