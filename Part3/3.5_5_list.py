from datetime import datetime

pat = '%d.%m.%Y'
n, name_lib, count = int(input()), [], 0

for _ in range(n):
    s = input()
    name, sdt = s[:-11], s[-10:]
    dt = datetime.strptime(sdt, pat)
    name_lib.append((name, dt))

name_lib = sorted(name_lib, key=lambda x: x[1])
old = name_lib[0][1]
for i in name_lib:
    if i[1] == old:
        count += 1
if count == 1:
    print(f'{old.strftime(pat)} {name_lib[0][0]}')
else:
    print(f'{old.strftime(pat)} {count}')

