def get_min_max(iterable):
    try:
        tmp = next(iter(iterable))
        min_item, max_item = tmp, tmp
        for i in iterable:
            if i < min_item:
                min_item = i
            elif i > max_item:
                max_item = i
        return min_item, max_item
    except:
        return None


